#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*dstc;
	char	*srcc;

	if ((dst == src) || n == 0)
		return (dst);
	if (!dst && !src)
		return (0);
	dstc = (char *)dst;
	srcc = (char *)src;
	while (n--)
		dstc[n] = srcc[n];
	return (dst);
}
